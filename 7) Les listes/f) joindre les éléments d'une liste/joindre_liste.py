# LISTE

# Pour joindre une liste en python on utilise la méthode "join"
# Pour se faire on donne d'abord le caractère avec lequel on veut joindre les éléments de la liste
# Ensuite on utilise la méthode "join" sur ce caractère
# Puis on donne la liste déléments qu'on joindre avec ce caractère
liste = ["python", "est", "un", "langage", "incroyable"]
resultat = " ".join(liste)
print(resultat)