# LES LISTES

# Les listes vides
# N.B: Une liste vide est toujours "False"
liste = []
print(liste)

# Une liste peut contenir toutes sortes d'éléments
# On peut avoir :
# Une liste de d'éléments de même type
liste = [1, 2 , 3 , 4, 5]
print(liste)
# Une liste d'éléments de types différents
liste = [250, "Python", True]
print(liste)

# N.B: Une liste n'est "True" que lorsqu'elle n'est pas vide