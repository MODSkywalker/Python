# LES BOUCLES

# La boucle for
# La boucle for sur une liste
liste = [1, 10, 5, 8, 6, 7, 4]
for i in liste:
    print(i)
# La boucle for sur une chaîne de caractère
langage = "Python"
for lettre in langage:
    print(lettre)
# Répeter rapidement une action plusieurs fois
for i in range(5):
    print("Bonjour")